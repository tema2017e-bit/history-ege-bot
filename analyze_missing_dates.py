def normalize(s):
    """Нормализует строку для сравнения"""
    s = s.lower().strip()
    s = re.sub(r'[^\w\s]', '', s)
    s = re.sub(r'\s+', ' ', s)
    return s

def find_match(year, event, existing_events):
    """Ищет совпадение среди существующих карточек"""
    event_norm = normalize(event)
    year_norm = normalize(year)
    
    for e in existing_events:
        # Сначала проверяем год
        if normalize(e['year']) == year_norm:
            e_norm = normalize(e['event'])
            # Проверяем ключевые слова
            event_words = set(event_norm.split())
            e_words = set(e_norm.split())
            common = event_words & e_words
            if len(common) >= 2:
                return e
    
    # Попробуем найти по ключевым словам
    for e in existing_events:
        e_norm = normalize(e['event'])
        event_words = set(event_norm.split())
        e_words = set(e_norm.split())
        common = event_words & e_words
        if len(common) >= 3:
            return e
    
    return None

