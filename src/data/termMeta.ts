// Вспомогательная: получить TermMeta по названию термина
export function getTermMeta(termName: string): TermMeta | undefined {
  return termMetaList.find(t => t.term === termName);
}

// Вспомогательная: получить все термины для эпохи
export function getTermMetaByEra(eraId: string): TermMeta[] {
  return termMetaList.filter(t => t.era === eraId);
}

// Вспомогательная: получить все уникальные эпохи, для которых есть мета-термины
export function getErasWithTermMeta(): string[] {
  return [...new Set(termMetaList.map(t => t.era))];
}
