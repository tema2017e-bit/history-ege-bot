export function getSectionById(id: string): TheorySection | undefined {
  return theorySections.find(s => s.id === id);
}

export function getTopicById(topicId: string): { section: TheorySection; topic: TheoryTopic } | undefined {
  for (const section of theorySections) {
    const topic = section.topics.find(t => t.id === topicId);
    if (topic) return { section, topic };
  }
  return undefined;
}

export function findTopicsByCardId(cardId: string): { section: TheorySection; topic: TheoryTopic }[] {
  const results: { section: TheorySection; topic: TheoryTopic }[] = [];
  for (const section of theorySections) {
    for (const topic of section.topics) {
      if (topic.relatedCardIds?.includes(cardId)) {
        results.push({ section, topic });
      }
    }
  }
  return results;
}